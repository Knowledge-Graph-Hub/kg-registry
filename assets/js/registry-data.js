/*
Registry data loading for KG-Registry.

This is a separate, dependency-free file, loaded from the TOP of <head> before
any stylesheet link. Both details matter (see issue #662):

  - custom.js runs inside jQuery(document).ready(), so a fetch there cannot
    start until the jQuery CDN request has downloaded and executed.
  - More importantly, a parser-inserted <script> must wait for all pending
    stylesheets before it executes. Loaded at the end of <body>, this file
    finished downloading at ~107ms but could not run until the CDN CSS
    resolved at ~3.9s. Placed before any <link rel=stylesheet>, there are no
    pending stylesheets, so it executes immediately and the request goes out
    while the CDN CSS and scripts are still downloading.

Keep this file dependency-free and keep its <script> tag above the stylesheets.
*/

(function () {
    'use strict';

    // Resolve data URLs from this script's own location rather than the page's,
    // so the site's baseurl is picked up without Liquid and the paths do not
    // depend on which page happens to include us.
    // .../assets/js/registry-data.js -> .../registry/kgs-summary.json
    var base = (document.currentScript && document.currentScript.src || '')
        .replace(/assets\/js\/registry-data\.js.*$/, '');

    // The main table only needs a few fields per resource, so it loads a slim
    // summary (generated by util/make_summary_json.py) rather than the full
    // ~30 MB kgs.jsonld. Fall back to the full file if the summary is
    // unavailable, e.g. on a deployment built before the summary existed.
    var SUMMARY_URL = base + 'registry/kgs-summary.json';
    var FULL_URL = base + 'registry/kgs.jsonld';

    function fetchJson(url) {
        return fetch(url).then(function (response) {
            if (!response.ok) {
                throw new Error(url + ': ' + response.status + ' ' + response.statusText);
            }
            return response.json();
        });
    }

    function fetchRegistryData() {
        return fetchJson(SUMMARY_URL).catch(function (error) {
            console.warn(
                'Could not load ' + SUMMARY_URL + ' (' + error.message + '); ' +
                'falling back to ' + FULL_URL + '. This is slower.'
            );
            return fetchJson(FULL_URL);
        });
    }

    var preloaded = fetchRegistryData();
    // Attach a handler now so a failure before custom.js consumes the promise
    // is not reported as an unhandled rejection. The rejection is still
    // delivered to the real consumer below.
    preloaded.catch(function () { });

    window.KGRegistryData = {
        fetch: fetchRegistryData,
        // The in-flight request started at page load. take() returns it once,
        // so a Retry after a failure issues a fresh request instead of
        // replaying the settled (rejected) promise.
        take: function () {
            var p = preloaded;
            preloaded = null;
            return p || fetchRegistryData();
        }
    };
})();
